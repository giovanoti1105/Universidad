package EjerciciosProyectosInformaticos;

public class DistanciaDoblePrecision extends Distancia
{
	double distancia;
	// constructor sin arguentos inicializa distancia en 0
	 
	// constructor con paso de  arguentos iguala distancia al valor de la variable 
	public DistanciaDoblePrecision(double distancia)
	{
		super(0);
		this.distancia= distancia;
	}
	@Override
	void incrementaDistancia(float incremento)
	{
		distancia+=incremento;
	}
}
