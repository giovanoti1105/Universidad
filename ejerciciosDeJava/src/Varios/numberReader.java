package Varios;
public class numberReader {
	String ConvertirNum(int val) {
		switch (val) {
		case 0:
			return "cero";
		case 1:
			return "uno ";
		case 2:
			return "dos";
		case 3:
			return "tres ";
		case 4:
			return "cuatro ";
		case 5:
			return "cinco";
		case 6:
			return "seis";
		case 7:
			return "siete";
		case 8:
			return "ocho";
		case 9:
			return "nueve";
		default:
			return " ";

		}
	}

	public static void main(String[] args) 
	{
		numberReader n = new numberReader();
		String num = n.ConvertirNum(4) + n.ConvertirNum(3) + n.ConvertirNum(1);
		System.out.println("413 convertido a " + num);
	}
}