package Varios.Guis;

import java.awt.FlowLayout;
import java.awt.LayoutManager;

import javax.swing.JButton;
import javax.swing.JFrame;

public class FLayout extends JFrame 
{
	// creacion de botones
	
	JButton a = new JButton("Abierto");
	JButton b = new JButton("Basico");
	JButton c = new JButton("Cerrado");
	JButton d = new JButton("Dejado");
	JButton e = new JButton("Escrito");
	JButton f = new JButton("Facil");
	JButton g = new JButton("Gastado");
	public FLayout()
	{
		// llamamos al constructor de jframe 
		super();
		setSize(360,120);
		setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
		FlowLayout flow = new FlowLayout(FlowLayout.LEFT);
		setLayout(flow);
		add(a);
		add(b);
		add(c);
		add(d);
		add(e);
		add(f);
		add(g);
		setVisible(true);
	}
	public static void main(String[] args)
	{
		new FLayout();
	}

}
