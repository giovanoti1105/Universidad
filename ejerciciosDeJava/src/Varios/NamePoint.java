package Varios;
import java.awt.Point;

class NamePoint extends Point
{
    // definicion de variable de instancia
    String name;
    // definicion de constructor con 1 argumento adicional que es el nombre
    NamePoint (int x, int y, String name)
    {
	super(x,y);
	this.name=name;
    }
    public static void main(String[] args)
    {
	NamePoint objnp = new NamePoint (5,5,"Nombre Objeto");
	System.out.println("x = : " + objnp.x);
	System.out.println("y = : " + objnp.y);
	System.out.println("El nombre del objeto es : " + objnp.name);
    }
}
