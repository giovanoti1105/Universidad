package SegundoSemestre.CalculadoraConControles;

import javax.swing.JOptionPane;

import com.sun.org.apache.bcel.internal.generic.RET;

public class Calculos
{
	private Calculadora objCalculadora;
	int uno;
	int dos;
	// constructor de la clase
	public Calculos(){}
	
	public String onClickBtnSuma(String uno, String dos)
	{
		int nrouno=Integer.parseInt(uno);
		int nrodos=Integer.parseInt(dos);
		int resultado= nrouno+nrodos;
		String respuesta = String.valueOf(resultado);
		return respuesta;
	}
	
	public String onClickBtnResta(String uno, String dos)
	{
		int nrouno=Integer.parseInt(uno);
		int nrodos=Integer.parseInt(dos);
		int resultado= nrouno-nrodos;
		String respuesta = String.valueOf(resultado);
		return respuesta;
	}
	
	public String onClickBtnMultiplicacion(String uno, String dos)
	{
		int nrouno=Integer.parseInt(uno);
		int nrodos=Integer.parseInt(dos);
		int resultado= nrouno*nrodos;
		String respuesta = String.valueOf(resultado);
		return respuesta;
	}
	public String onClickBtnDivision(String uno, String dos)
	{
		int nrouno=Integer.parseInt(uno);
		int nrodos=Integer.parseInt(dos);
		int resultado= nrouno/nrodos;
		String respuesta = String.valueOf(resultado);
		return respuesta;
	}
}