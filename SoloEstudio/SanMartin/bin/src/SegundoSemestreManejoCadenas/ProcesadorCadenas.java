package SegundoSemestreManejoCadenas;

public class ProcesadorCadenas
{
	public ProcesadorCadenas(){ }
	
	public String invertirCadenas(String cadena)
	{
		//ManejoCadenas objcadenas = new ManejoCadenas();
		String cadenaInvertida="";
		for (int i= cadena.length()-1;i>=0;i--)
		{
			cadenaInvertida+=cadena.charAt(i);
		}
		return cadenaInvertida;
	}
	public String cadenaMasLarga(String cadena1, String cadena2)
	{
		String cadenaMasLarga="";
		
		if (cadena1.length() > cadena2.length())
			cadenaMasLarga = cadena1;
		else
			cadenaMasLarga = cadena2;
		return cadenaMasLarga;
	}
}
