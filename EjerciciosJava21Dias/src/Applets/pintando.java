package Applets;

import java.applet.Applet;
import java.awt.Color;
import java.awt.Font;
import java.awt.Graphics;

public class pintando extends Applet
{
	Font f = new Font("TimesRoman", Font.ITALIC, 48);
	
	public pintando()
	{
	}
	public void paint(Graphics graficos)
	{
		graficos.setFont(f);
		graficos.setColor(Color.red);
		graficos.drawString("Valor del aplet", 5, 40);
		graficos.drawOval(30, 40, 101, 202);
	}
}