package Applets;

import java.awt.Color;
import java.awt.Font;
import java.awt.GradientPaint;
import java.awt.Graphics;

public class Palindrome extends java.applet.Applet
{
    // creacion de objeto de la clase Font precargado con argumentos 
    Font objfont = new Font("TimesRoman",Font.BOLD,36);
    // creacion del metodo pain
    public void Paint (Graphics screen)
    {
	screen.setFont(objfont);
	screen.setColor(Color.red);
	screen.drawString("Bienvenidos ", 1, 1);
    }
    public void init ()
    {
    	paint(null);
    }
}
